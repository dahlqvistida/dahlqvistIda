
public class uppgifter {

	public static void main(String[] args) {
		
		
		//skapa ett program som skriver ut alla jämna heltal mellan 100 till 1000
		for (int i = 100; i <= 1000; i += 2) {
		System.out.println(i); 
		}

		System.out.println("\t");
		
		//skapa ett program som skriver ut alla heltal mellan 25 till 50.
		for (int i = 25; i <= 50; i++) {
			System.out.print(i+" ");
		
		}
		
		System.out.println("\t");
		//skriver ut alla positiva udda heltal från 20 till 0
		for (int i = 19; i > 0; i -= 2) {
			System.out.print(i + "  ");
	}

	}
		

}
